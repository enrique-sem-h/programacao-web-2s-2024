// adicao
let n1 = 3;
let n2 = 5;
let result = n1 + n2;
console.log(`Resultado da adicao entre ${n1} e ${n2} = ${result}`);

// subtracao 
let n1Sub = 6;
let n2Sub = 5;
let resultSub = n1Sub - n2Sub;
console.log(`Resultado da subtracao entre ${n1Sub} e ${n2Sub} = ${resultSub}`);

// multiplicacao
let n1Mult = 6;
let n2Mult = 5;
let resultMult = n1Mult * n2Mult;
console.log(`Resultado da multiplicacao entre ${n1Mult} e ${n2Mult} = ${resultMult}`);

// divisao
let n1Div = 6;
let n2Div = 3;
let resultDiv = n1Div / n2Div;
console.log(`Resultado da divisao entre ${n1Div} e ${n2Div} = ${resultDiv}`);